package algorithms;

class CellTrace {
	
	protected Cell c;
	protected StringBuilder partialA;
	protected StringBuilder partialB;
	
	public CellTrace(Cell c, StringBuilder partialA, StringBuilder partialB) {
		this.c = c;
		this.partialA = partialA;
		this.partialB = partialB;
	}
	
	
	
}
